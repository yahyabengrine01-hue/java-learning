package day2;

public class datatypedemo {

	public static void main(String[] args) {
		//numeric data type 
		int a=10,b=20;
		System.out.println(" the value of a is :"+a );
		System.out.println(" the value of a is :"+b );
		System.out.println(" the sum of a+b is :"+(b+a) );
		
	    byte c=125;
	    System.out.println(c);
	    short e=22;
	    //long,short,byte,int; long need l
	    //decimal numbers : float,double ; 
	    float price=10.5f; //float need f 
	    System.out.println(price);
	    
	    double discount=55.8897;
	    System.out.println(discount);

	    
	    char grade='A';
	    System.out.println(grade);//single character

	    String fullName="lionel messi";//multiple character 
	    System.out.println(fullName);
	    
	    boolean A=true;//allows only true or false 
	    System.out.println(A);
	    
	    System.out.println("the number 1 and have a grade A on this world is lionel messi and this answer is true");
	    
		
	}

}
