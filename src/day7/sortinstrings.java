package day7;

import java.util.Arrays;

public class sortinstrings {

	public static void main(String[] args) {
		String barca[]={ "pedri","yamal","rafinha","cubarsi",};
		
		System.out.println("befor sorting : "+Arrays.toString(barca));
		Arrays.sort(barca);
		System.out.println("after sorting : "+Arrays.toString(barca));
		

	}

}
